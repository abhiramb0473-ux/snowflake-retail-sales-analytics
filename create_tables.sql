CREATE OR REPLACE DATABASE RETAIL_DB;
CREATE OR REPLACE SCHEMA RETAIL_DB.SALES;
USE DATABASE RETAIL_DB;
USE SCHEMA SALES;

CREATE OR REPLACE TABLE CUSTOMERS (
  CUSTOMER_ID INT,
  CUSTOMER_NAME STRING,
  REGION STRING
);

CREATE OR REPLACE TABLE PRODUCTS (
  PRODUCT_ID INT,
  PRODUCT_NAME STRING,
  CATEGORY STRING,
  PRICE FLOAT
);

CREATE OR REPLACE TABLE ORDERS (
  ORDER_ID INT,
  CUSTOMER_ID INT,
  PRODUCT_ID INT,
  ORDER_DATE DATE,
  QUANTITY INT
);
